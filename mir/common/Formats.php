    /**
     * @param int $number
     * @param array $ImRodRoded ['день', 'дней', 'дня']
     * @return string
     */
    public static function numbersRusPadegRod(int $number, array $ImRodRoded)
    {
        $ostDel = $number % 10;
        if ($number > 5 && $number < 21 || in_array($ostDel, [5, 6, 7, 8, 9, 0])) {
            return $ImRodRoded[1];
        } elseif ($ostDel === 1) {
            return $ImRodRoded[0];
        } elseif (in_array($ostDel, [2, 3, 4])) {
            return $ImRodRoded[2];
        }
    }

